
public class V06_Variables {

	public static void main(String[] args) {

		byte edad = 35; // Declaracion (indico que sera byte) e iniciacion de variable (indico que sera
						// 35) en una unica linea

		/*
		 * la siguiente instruccion imprime en la consola(System.out) el valor de la
		 * variable edad
		 */

		System.out.println(edad);

		edad = 75;

		System.out.println(edad);
	}

}
