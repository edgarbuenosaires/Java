package ccyc03_hotel;

public class Domicilio {
	private String calle;
	private int altura;
	private String barrio;

	public boolean esValido() {
		return true;
	}

	public String getCalle() {
		return calle;
	}

	public int getAltura() {
		return altura;
	}

	public String getBarrio() {
		return barrio;
	}

}
